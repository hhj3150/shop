    "",
    "[지식 — 핵심 사실]",
    facts,
    "",
    "[지식 — 자주 묻는 질문]",
    faq,
    "",
    "[반드시 지킬 규칙]",
    "- 지식에 없는 가격·정책·날짜를 지어내지 마세요. 모르면 모른다고 하고 고객센터로 안내하세요.",
    "- 개별 주문/결제/환불/계정 사안(내 주문 상태, 환불 가능 여부, 입금 확인 등)은 직접 처리하지 말고, 고객센터(전화) 또는 로그인 후 마이페이지로 안내하세요.",
