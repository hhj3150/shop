// 입금자명 정규화 — 클라/서버 공용(서버전용 모듈 비의존, phone.ts 와 동일 패턴).
//
// 배경: PayAction 자동매칭은 '등록된 입금자명' 과 '실제 통장에서 찍혀 들어온 보내는분 이름' 을
//   글자 단위로 대조한다. 사용자가 입금자명 칸에 덧붙인 괄호 메모(예: "이경원원장(98예준)") 는
//   통장엔 절대 안 찍히므로, 등록명에 그 메모가 남으면 이름이 달라져 자동매칭이 실패한다(수동매칭 필요).
//
// 규칙: 이름 뒤에 붙은 괄호·대괄호 메모를 제거하고 공백을 한 칸으로 정리한다.
//   단, 맨 앞 괄호(예 "(주)디투오")는 상호의 일부일 수 있어 보존한다(비-선두 괄호만 제거).
//   - "이경원원장(98예준)" → "이경원원장"
//   - "홍길동 [메모]"      → "홍길동"
//   - "(주)디투오"         → "(주)디투오"  (선두 괄호 보존)
export function normalizeBillingName(raw: string | null | undefined): string {
  return (raw ?? "")
    // 비-선두 괄호/대괄호 메모 제거: 앞에 비공백 글자가 있을 때만(선두 "(주)…" 는 보존).
    //   바로 앞 공백까지 함께 흡수해 "이름 (메모)" 의 잔여 공백을 남기지 않는다.
    .replace(/(?<=\S)\s*[(（\[【][^)）\]】]*[)）\]】]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}
